#!/usr/bin/env bash
# Write or update a per-plan status issue (Phase 5 Task 5.1).
#
# Usage: plan-status.sh <state_file> [<owner/repo>]
#
# Idempotent: looks for an existing `[plan-NN] status` issue (matched by
# exact title); creates one if missing, edits its body if present.
#
# Body sections:
#   - Plan header: name, status, counts by lifecycle state
#   - Task table: status, issue link, PR link, retries
#   - Mermaid dep graph (skipped if all tasks are dep-free)
#   - In-flight PRs list (status == in_review)
#
# Triggering: orchestrator.sh invokes this at the end of every tick.
# Failures here are non-fatal — the dashboard is operator UX, not a
# correctness gate.
#
# Exit codes:
#   0  status issue written/updated (or no-op on archive)
#   1  environment/args failure

set -uo pipefail

command -v jq >/dev/null || { echo "plan-status: jq required" >&2; exit 1; }
command -v gh >/dev/null || { echo "plan-status: gh required" >&2; exit 1; }

if [ $# -lt 1 ]; then
  echo "usage: $0 <state_file> [<owner/repo>]" >&2
  exit 1
fi

STATE_FILE="$1"
REPO="${2:-$(gh repo view --json nameWithOwner -q .nameWithOwner 2>/dev/null || true)}"

[ -f "$STATE_FILE" ] || { echo "plan-status: state file not found: $STATE_FILE" >&2; exit 1; }
[ -n "$REPO" ] || { echo "plan-status: no repo specified and gh autodetect failed" >&2; exit 1; }

jq -e '.tasks | type == "object"' "$STATE_FILE" >/dev/null 2>&1 || {
  echo "plan-status: state file lacks .tasks object (v2 schema)" >&2
  exit 1
}

PLAN_FILE=$(jq -r '.plan_file' "$STATE_FILE")
PLAN_STATUS=$(jq -r '.status' "$STATE_FILE")
TOTAL=$(jq -r '.total_tasks' "$STATE_FILE")
AUTO_REC=$(jq -r 'if has("auto_recommended") then .auto_recommended else false end' "$STATE_FILE")
PLAN_BASE=$(basename "$PLAN_FILE" .md)
PLAN_NUM=$(echo "$PLAN_BASE" | grep -oE 'PLAN-[0-9]+' | grep -oE '[0-9]+' || echo "00")

TITLE="[plan-${PLAN_NUM}] status"
PLAN_LABEL="orch:plan-${PLAN_NUM}"

# Counts by status (any unrecognized status counted as "other")
COUNTS=$(jq -r '
  reduce (.tasks | to_entries[]) as $e (
    {pending:0, in_progress:0, in_review:0, merged:0, blocked:0, other:0};
    .[$e.value.status // "other"] = ((.[$e.value.status // "other"] // 0) + 1)
  )
' "$STATE_FILE")
PENDING=$(echo "$COUNTS" | jq -r '.pending // 0')
IN_PROGRESS=$(echo "$COUNTS" | jq -r '.in_progress // 0')
IN_REVIEW=$(echo "$COUNTS" | jq -r '.in_review // 0')
MERGED=$(echo "$COUNTS" | jq -r '.merged // 0')
BLOCKED=$(echo "$COUNTS" | jq -r '.blocked // 0')

# Task table rows. Issue/PR cells are GitHub-auto-linked via #N syntax.
TASK_ROWS=$(jq -r --arg repo "$REPO" '
  .tasks | to_entries | sort_by(.key | tonumber) | .[] |
  "| \(.key) | \(.value.status) | " +
  (if .value.issue then "#\(.value.issue)" else "—" end) + " | " +
  (if .value.pr then "#\(.value.pr)" else "—" end) + " | " +
  "\(.value.retries // 0) | " +
  (.value.title // "") + " |"
' "$STATE_FILE")

# Mermaid dep graph. Skip if no dependencies anywhere — a graph of
# disconnected nodes adds noise without insight.
HAS_DEPS=$(jq -r '[.tasks[].depends_on // [] | length] | add > 0' "$STATE_FILE")
MERMAID_BLOCK=""
if [ "$HAS_DEPS" = "true" ]; then
  MERMAID_BODY=$(jq -r '
    .tasks | to_entries | sort_by(.key | tonumber) as $entries |
    (
      [$entries[] | "  T\(.key)[\"\(.key): " + ((.value.title // "") | gsub("\""; "\\\"")) + "\"]"]
      + [$entries[] | .key as $k | (.value.depends_on // [])[] | "  T\($k) --> T\(.)"]
    ) | .[]
  ' "$STATE_FILE")
  MERMAID_BLOCK=$(printf "### Dependency graph\n\n\`\`\`mermaid\ngraph TD\n%s\n\`\`\`\n" "$MERMAID_BODY")
fi

# In-flight PRs: status == in_review with .pr set
INFLIGHT=$(jq -r '
  .tasks | to_entries | sort_by(.key | tonumber) | .[] |
  select(.value.status == "in_review" and .value.pr != null) |
  "- #\(.value.pr) — task \(.key): " + (.value.title // "")
' "$STATE_FILE")
INFLIGHT_BLOCK=""
if [ -n "$INFLIGHT" ]; then
  INFLIGHT_BLOCK=$(printf "### In-flight PRs\n\n%s\n" "$INFLIGHT")
fi

UPDATED=$(date -u +%Y-%m-%dT%H:%M:%SZ)
AUTO_REC_LINE=""
[ "$AUTO_REC" = "true" ] && AUTO_REC_LINE=" · auto_recommended: **on**"

BODY=$(cat <<EOF
## Plan \`${PLAN_BASE}\` — status: \`${PLAN_STATUS}\`

**Tasks:** ${TOTAL} total · ${MERGED} merged · ${IN_REVIEW} in_review · ${IN_PROGRESS} in_progress · ${BLOCKED} blocked · ${PENDING} pending
**Updated:** ${UPDATED}${AUTO_REC_LINE}

### Tasks

| # | Status | Issue | PR | Retries | Title |
|---|---|---|---|---|---|
${TASK_ROWS}

${MERMAID_BLOCK}
${INFLIGHT_BLOCK}

---
*Auto-generated by orchestrator. Do not edit — body is overwritten every tick.*
EOF
)

# Look for existing status issue (exact title match in repo).
# Search uses fulltext + filter; cheap on small repos but may need
# pagination on large ones — accept that limitation for v1.
EXISTING=$(gh issue list \
  --repo "$REPO" \
  --search "\"$TITLE\" in:title" \
  --state all \
  --json number,title \
  --jq ".[] | select(.title == \"$TITLE\") | .number" 2>/dev/null | head -1)

if [ -z "$EXISTING" ]; then
  echo "plan-status: creating new status issue '$TITLE'"
  CREATED=$(gh issue create \
    --repo "$REPO" \
    --title "$TITLE" \
    --body "$BODY" \
    --label "$PLAN_LABEL" 2>&1) || {
      echo "plan-status: gh issue create failed: $CREATED" >&2
      exit 1
    }
  echo "plan-status: created $CREATED"
else
  echo "plan-status: updating issue #$EXISTING ($TITLE)"
  gh issue edit "$EXISTING" --repo "$REPO" --body "$BODY" >/dev/null 2>&1 || {
    echo "plan-status: gh issue edit failed on #$EXISTING" >&2
    exit 1
  }
fi

exit 0
